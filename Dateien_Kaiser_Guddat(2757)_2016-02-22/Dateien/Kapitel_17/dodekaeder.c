void hamilton( int anz, int array[], int start )
   {
   int i, sav;

   if( start == anz )
      {
      if( dodekaeder[array[anz - 1]][array[0]] )
         {
         for( i = 0; i < anz; i++ )
            printf( "%d-", array[i] );
         printf( "%d\n", array[0] );
         }
      }
   else
      {
      sav = array[start];
      for( i = start; i < anz; i++ )
         {
         array[start] = array[i];
         array[i] = sav;
         if( dodekaeder[array[start - 1]][array[start]] )
            hamilton( anz, array, start + 1 );
         array[i] = array[start];
         }
      array[start] = sav;
      }
   }

